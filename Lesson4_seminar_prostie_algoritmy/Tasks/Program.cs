// РЕШЕНИЕ

int num1 = Convert.ToInt32(value: Console.ReadLine());
int num2 = Convert.ToInt32(value: Console.ReadLine());

if (num1 % num2 != 0)
{
    Console.WriteLine(value: $"Число {num2} не является кратным {num1}! Остаток от деления: {num1 % num2}");
}
else
{
    Console.WriteLine(value: $"Число {num2} является кратным {num1}!");
}